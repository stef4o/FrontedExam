function Novel(id, title, author, publisher, yearOfPublication, length, series, seriesNumber, ISBN, review) {
    this.kind = "novel";
    this.id = id;
    this.title = title;
    this.author = author;
    this.publisher = publisher;
    this.yearOfPublication = yearOfPublication;
    this.length = length;
    this.series = series;
    this.seriesNumber = seriesNumber;
    this.ISBN = ISBN;
    this.review = review;
}